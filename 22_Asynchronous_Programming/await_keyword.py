# await_keyword.py

"""
Definition: The 'await' keyword is used to pause the execution of an asynchronous function until a specific task is complete, allowing other tasks to run in the meantime.
"""

def simulate_await():
    print("Async Function: Starting...")
    print("Awaiting Task (1 second)...")
    # Simulation of pause
    print("Task Complete.")
    print("Async Function: Resumed and Finished.")

simulate_await()
