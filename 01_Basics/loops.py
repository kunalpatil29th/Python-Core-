# loops.py

"""
Definition: Loops (for, while) are used to iterate over a sequence of items 
or execute a block of code repeatedly while a condition is True.
"""

# For loop
print("For loop example:")
for i in range(5):
    print(i)

# While loop
print("\nWhile loop example:")
count = 0
while count < 5:
    print(count)
    count += 1
